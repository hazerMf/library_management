    private void showAll(){
        model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for(Manager i : manager_list){
            model.addRow(new Object[]{
                i.getId(), i.getName(), i.getPhone(), i.getEmail()
            });
        }
    }
    
    private void showOne(String s){
        model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        for(Manager i : manager_list){
            if(i.getId().equals(s)){
                model.addRow(new Object[]{
                    i.getId(), i.getName(), i.getPhone(), i.getEmail()
                });
            }
        }
    }
    
