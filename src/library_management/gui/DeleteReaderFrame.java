    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        
        if(id.equals("")){
            JOptionPane.showMessageDialog(this, "Reader ID can not be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(!checkReader(id)){
                JOptionPane.showMessageDialog(this, "Reader not exist.", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                for(Reader i : reader_list){
                    if(i.getId().equals(id)){
                        if(i.getTotalFine()!=0){
                            JOptionPane.showMessageDialog(this, "This reader can not be removed.", "Error", JOptionPane.ERROR_MESSAGE);
                        }else{
                            m.deleteReader(id);
                            JOptionPane.showMessageDialog(this, "Reader succesfully deleted!", "Success", JOptionPane.INFORMATION_MESSAGE);
                            jTextField1.setText("");
                            showAll();
                        }
                    }
                }
            }
                
        }
    }//GEN-LAST:event_jButton1ActionPerformed

