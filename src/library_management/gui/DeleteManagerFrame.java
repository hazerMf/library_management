    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // check reader info
        String id = jTextField1.getText();

        if(id.equals("")){
            showAll();
        }else{

            //show reader infomation
            if(!checkManager(id)){
                JOptionPane.showMessageDialog(this, "Manager not exist.", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                showOne(id);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

