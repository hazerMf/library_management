    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String id = jTextField1.getText();
        
        if(id.equals("")){
            JOptionPane.showMessageDialog(this, "Manager ID can not be empty.", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(!checkManager(id)){
                JOptionPane.showMessageDialog(this, "Manager ID not exist.", "Error", JOptionPane.ERROR_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Manager succesfully returned!", "Success", JOptionPane.INFORMATION_MESSAGE);
                m.deleteManager(id);
                showAll();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

